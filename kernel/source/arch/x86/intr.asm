BITS 32
SECTION .text

%macro ISR_NO_ERR 1
GLOBAL isr_%1
isr_%1:
	PUSH DWORD 0
	PUSH DWORD %1
	JMP isr_common
%endmacro

%macro ISR_ERR 1
GLOBAL isr_%1
isr_%1:
	;; A CPU j√° empurra o erro aqui
	;; PUSH DWORD 0
	PUSH DWORD %1
	JMP isr_common
%endmacro

EXTERN intr_handler
GLOBAL isr_common
isr_common:
	PUSHAD

	XOR EAX, EAX
	MOV AX, GS
	PUSH EAX
	MOV AX, FS
	PUSH EAX
	MOV AX, ES
	PUSH EAX
	MOV AX, DS
	PUSH EAX

	PUSH ESP
	CALL intr_handler
	ADD ESP, 4 ;; Remover o parametro passado

	POP EAX
	MOV DS, AX
	POP EAX
	MOV ES, AX
	POP EAX
	MOV FS, AX
	POP EAX
	MOV GS, AX

	POPAD

	ADD ESP, 8 ;; Remover int_no e err_code
	IRET

